
public class EstruturasCondicionaisEx6 {

	public static void main(String[] args) {
		int x = 3;
		switch (x) {
			case 1 : {
				System.out.println("x vale 1");	
				break;
			}
			case 2 : 
				System.out.println("x vale 2");	
			default:
				System.out.println("x é maior que 2");
				break;
			
		}

	}

}
